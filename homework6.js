function findValueByKey(Object, companyName)
{
	if(companyName == Object.name)
	{
		console.log(Object);
		return Object;
	}
	else
	{
		let arr = Object.clients || Object.partners;
		
		for(let i in arr)
		{
			if(String(companyName).includes(arr[i].name))
			{
				Object = arr[i];
				break;
			}
		}

		return findValueByKey(Object, companyName);		
	}
}

